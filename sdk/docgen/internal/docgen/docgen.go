func renderSDKArea(root, title, intro string, symbols []string) (string, error) {
	d, fset, err := loadSDKDoc(root)
	if err != nil {
		return "", err
	}
	idx := indexDoc(d)
	var b strings.Builder
	b.WriteString(header("sdk/go (go doc)"))
	fmt.Fprintf(&b, "\n# %s\n\n", title)
	b.WriteString(provenance("sdk/go (go doc)"))
	b.WriteString("\n" + intro + "\n\n")
	rend := commentRenderer(d)
	for _, name := range symbols {
		s, ok := renderSymbol(fset, idx, name, rend)
		if !ok {
			return "", fmt.Errorf("symbol %q not found in sdk/go", name)
		}
		b.WriteString(s)
	}
	return b.String(), nil
}

// commentRenderer renders a Go doc comment string to markdown via go/doc/comment,
// which escapes text that would otherwise be read as markdown or HTML (e.g. the
// `<id>` / `<sub>` placeholders in subject patterns) and renders lists and links.
func commentRenderer(d *doc.Package) func(string) string {
	parser := d.Parser()
	pr := &comment.Printer{}
	return func(text string) string {
		if strings.TrimSpace(text) == "" {
			return ""
		}
		return strings.TrimSpace(string(pr.Markdown(parser.Parse(text))))
	}
}

func loadSDKDoc(root string) (*doc.Package, *token.FileSet, error) {
	fset := token.NewFileSet()
	dir := filepath.Join(root, "sdk", "go")
	entries, err := os.ReadDir(dir)
	if err != nil {
		return nil, nil, err
	}
	// Parse the package's non-test .go files into the AST set doc.NewFromFiles
	// needs (parser.ParseDir was deprecated in Go 1.25; this is the stdlib
	// replacement that keeps the tool dependency-free).
	var files []*ast.File
	for _, e := range entries {
		name := e.Name()
		if e.IsDir() || !strings.HasSuffix(name, ".go") || strings.HasSuffix(name, "_test.go") {
			continue
		}
		f, perr := parser.ParseFile(fset, filepath.Join(dir, name), nil, parser.ParseComments)
		if perr != nil {
			return nil, nil, perr
		}
		if f.Name.Name != "sextant" {
			continue
		}
		files = append(files, f)
	}
	if len(files) == 0 {
		return nil, nil, fmt.Errorf("package sextant not found in %s", dir)
	}
	d, err := doc.NewFromFiles(fset, files, "github.com/love-lena/sextant/sdk/go")
	if err != nil {
		return nil, nil, fmt.Errorf("build SDK doc from %s: %w", dir, err)
	}
	return d, fset, nil
}

type docIndex struct {
	types   map[string]*doc.Type
	funcs   map[string]*doc.Func
	methods map[string]*doc.Func // "Recv.Name"
}

func indexDoc(d *doc.Package) docIndex {
	idx := docIndex{
		types:   map[string]*doc.Type{},
		funcs:   map[string]*doc.Func{},
		methods: map[string]*doc.Func{},
	}
	for _, f := range d.Funcs {
		idx.funcs[f.Name] = f
	}
	for _, t := range d.Types {
		idx.types[t.Name] = t
		for _, f := range t.Funcs {
			idx.funcs[f.Name] = f
		}
		for _, m := range t.Methods {
			idx.methods[m.Recv+"."+m.Name] = m
			idx.methods[strings.TrimPrefix(m.Recv, "*")+"."+m.Name] = m
		}
	}
	return idx
}

func renderSymbol(fset *token.FileSet, idx docIndex, name string, rend func(string) string) (string, bool) {
	if strings.Contains(name, ".") {
		if m, ok := idx.methods[name]; ok {
			return renderFunc(fset, m, 2, rend), true
		}
		return "", false
	}
	if t, ok := idx.types[name]; ok {
		return renderType(fset, t, 2, false, rend), true
	}
	if f, ok := idx.funcs[name]; ok {
		return renderFunc(fset, f, 2, rend), true
	}
	return "", false
}

func renderType(fset *token.FileSet, t *doc.Type, level int, withMembers bool, rend func(string) string) string {
	var b strings.Builder
	fmt.Fprintf(&b, "%s type `%s`\n\n", strings.Repeat("#", level), t.Name)
	b.WriteString(codeBlock(declString(fset, t.Decl)))
	if d := rend(t.Doc); d != "" {
		b.WriteString(d + "\n\n")
	}
	if withMembers {
		for _, f := range t.Funcs {
			b.WriteString(renderFunc(fset, f, level+1, rend))
		}
		for _, m := range t.Methods {
			b.WriteString(renderFunc(fset, m, level+1, rend))
		}
	}
	return b.String()
}

func renderFunc(fset *token.FileSet, f *doc.Func, level int, rend func(string) string) string {
	var b strings.Builder
	label := f.Name
	if f.Recv != "" {
		label = "(" + f.Recv + ") " + f.Name
	}
	fmt.Fprintf(&b, "%s func `%s`\n\n", strings.Repeat("#", level), label)
	b.WriteString(codeBlock(funcSignature(fset, f.Decl)))
	if d := rend(f.Doc); d != "" {
		b.WriteString(d + "\n\n")
	}
	return b.String()
}

// funcSignature prints a func declaration without its body.
func funcSignature(fset *token.FileSet, decl *ast.FuncDecl) string {
	if decl == nil {
		return ""
	}
	sig := *decl
	sig.Body = nil
	sig.Doc = nil
	return declString(fset, &sig)
}

func declString(fset *token.FileSet, node ast.Node) string {
	if node == nil {
		return ""
	}
	if gd, ok := node.(*ast.GenDecl); ok {
		clone := *gd
		clone.Doc = nil
		node = &clone
	}
	var buf bytes.Buffer
	cfg := printer.Config{Mode: printer.UseSpaces | printer.TabIndent, Tabwidth: 4}
	_ = cfg.Fprint(&buf, fset, node)
	return buf.String()
}

func codeBlock(code string) string {
	code = strings.TrimRight(code, "\n")
	if code == "" {
		return ""
	}
	return "```go\n" + code + "\n```\n\n"
}

// --- shared helpers ---------------------------------------------------------

type pair struct {
	Key string
	Val json.RawMessage
}

// orderedPairs decodes a JSON object preserving key order.
func orderedPairs(raw json.RawMessage) ([]pair, error) {
	if len(bytes.TrimSpace(raw)) == 0 {
		return nil, nil
	}
	dec := json.NewDecoder(bytes.NewReader(raw))
	tok, err := dec.Token()
	if err != nil {
		return nil, err
	}
	if d, ok := tok.(json.Delim); !ok || d != '{' {
		return nil, fmt.Errorf("expected JSON object, got %v", tok)
	}
	var out []pair
	for dec.More() {
		keyTok, err := dec.Token()
		if err != nil {
			return nil, err
		}
		key, ok := keyTok.(string)
		if !ok {
			return nil, fmt.Errorf("expected string key, got %v", keyTok)
		}
		var val json.RawMessage
		if err := dec.Decode(&val); err != nil {
			return nil, err
		}
		out = append(out, pair{key, val})
	}
	return out, nil
}

// cell makes a string safe for a single markdown table cell.
func cell(s string) string {
	s = strings.ReplaceAll(s, "|", "\\|")
	s = strings.ReplaceAll(s, "\n", " ")
	return strings.TrimSpace(s)
}
