// renderCanonMarkdown wraps an already-prose canon markdown file with the
// generated-page banner and provenance note, leaving its body verbatim.
func renderCanonMarkdown(root, rel string) (string, error) {
	raw, err := os.ReadFile(filepath.Join(root, filepath.FromSlash(rel)))
	if err != nil {
		return "", err
	}
	body := string(raw)
	// Insert the provenance note just after the title's first H1 line.
	var b strings.Builder
	b.WriteString(header(rel))
	lines := strings.SplitN(body, "\n", 2)
	b.WriteString("\n" + lines[0] + "\n\n")
	b.WriteString(provenance(rel))
	if len(lines) > 1 {
		b.WriteString("\n" + lines[1])
	}
	if !strings.HasSuffix(b.String(), "\n") {
		b.WriteString("\n")
	}
	return b.String(), nil
}

// --- Go SDK pages (go/doc over pkg/sextant) ---------------------------------

func genSDKReference(root string) (string, error) {
	d, fset, err := loadSDKDoc(root)
	if err != nil {
		return "", err
	}
	var b strings.Builder
	b.WriteString(header("pkg/sextant (go doc)"))
	b.WriteString("\n# API reference\n\n")
	b.WriteString(provenance("pkg/sextant (go doc)"))
	b.WriteString("\n")
	rend := commentRenderer(d)
	if dd := rend(d.Doc); dd != "" {
		b.WriteString(dd + "\n\n")
	}
	for _, f := range d.Funcs {
		b.WriteString(renderFunc(fset, f, 2, rend))
	}
	for _, t := range d.Types {
		b.WriteString(renderType(fset, t, 2, true, rend))
	}
	return b.String(), nil
}

