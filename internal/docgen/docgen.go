// renderLexicon renders one lexicon file. depth 0 renders the description and
// field table inline (one lexicon per page); depth >0 renders an `id` heading
// at that level first (many lexicons per page).
func renderLexicon(root, file string, depth int) (string, error) {
	raw, err := os.ReadFile(filepath.Join(root, "protocol", "lexicons", file))
	if err != nil {
		return "", err
	}
	var lf struct {
		ID   string `json:"id"`
		Defs struct {
			Main json.RawMessage `json:"main"`
		} `json:"defs"`
	}
	if err := json.Unmarshal(raw, &lf); err != nil {
		return "", err
	}
	var main struct {
		Description string          `json:"description"`
		Record      json.RawMessage `json:"record"`
		Required    []string        `json:"required"`
		Properties  json.RawMessage `json:"properties"`
	}
	if err := json.Unmarshal(lf.Defs.Main, &main); err != nil {
		return "", err
	}
	desc := main.Description
	required := main.Required
	properties := main.Properties
	if len(bytes.TrimSpace(main.Record)) > 0 {
		var rec struct {
			Required   []string        `json:"required"`
			Properties json.RawMessage `json:"properties"`
		}
		if err := json.Unmarshal(main.Record, &rec); err != nil {
			return "", err
		}
		required = rec.Required
		properties = rec.Properties
	}

	var b strings.Builder
	if depth > 0 {
		fmt.Fprintf(&b, "%s `%s`\n\n", strings.Repeat("#", depth), lf.ID)
	}
	if desc != "" {
		b.WriteString(strings.TrimSpace(desc) + "\n\n")
	}
	tbl, err := propertyTable(properties, required)
	if err != nil {
		return "", err
	}
	b.WriteString(tbl + "\n")
	return b.String(), nil
}

// propertyTable renders a lexicon properties object as a markdown table,
// preserving authored property order.
func propertyTable(raw json.RawMessage, required []string) (string, error) {
	pairs, err := orderedPairs(raw)
	if err != nil {
		return "", err
	}
	if len(pairs) == 0 {
		return "_No fields._\n", nil
	}
	req := make(map[string]bool, len(required))
	for _, r := range required {
		req[r] = true
	}
	var b strings.Builder
	b.WriteString("| Field | Type | Required | Description |\n|---|---|---|---|\n")
	for _, p := range pairs {
		var pv struct {
			Type        string   `json:"type"`
			Description string   `json:"description"`
			KnownValues []string `json:"knownValues"`
			Format      string   `json:"format"`
		}
		if err := json.Unmarshal(p.Val, &pv); err != nil {
			return "", err
		}
		typ := pv.Type
		if pv.Format != "" {
			typ += fmt.Sprintf(" (%s)", pv.Format)
		}
		if len(pv.KnownValues) > 0 {
			typ += " · one of: " + strings.Join(pv.KnownValues, ", ")
		}
		yes := ""
		if req[p.Key] {
			yes = "yes"
		}
		fmt.Fprintf(&b, "| `%s` | %s | %s | %s |\n", p.Key, cell(typ), yes, cell(pv.Description))
	}
	return b.String(), nil
}

