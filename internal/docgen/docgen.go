// Package docgen renders the mdbook reference's generated pages from canon:
// the protocol surface (protocol/methods.json + protocol/lexicons/*.json), the
// backend-neutral implementer docs (protocol/semantic-contract.md and
// protocol/wire-api.md), and the Go SDK API (go/doc over pkg/sextant).
//
// It writes only the generated pages under docs/book/src/; the hand-written
// prose pages are never touched. Output is deterministic so the mdbook CI job
// can regenerate and fail on any diff — the docs cannot silently drift from the
// canon they are generated from (the same up-to-date guard the TS codegen uses).
package docgen

import (
	"bytes"
	"encoding/json"
	"fmt"
	"go/ast"
	"go/doc"
	"go/doc/comment"
	"go/parser"
	"go/printer"
	"go/token"
	"os"
	"path/filepath"
	"strings"
)

// Run regenerates every generated page. root is the repo root; if empty, it is
// located by walking up from the working directory to the go.mod.
func Run(root string) error {
	if root == "" {
		r, err := findRoot()
		if err != nil {
			return err
		}
		root = r
	}
	pages := []struct {
		rel     string
		content func(string) (string, error)
	}{
		{"protocol/operations.md", genOperations},
		{"protocol/lexicons.md", genRecordLexicons},
		{"protocol/frame.md", genFrame},
		{"protocol/registry.md", genRegistry},
		{"implementers/backend.md", genBackend},
		{"implementers/wire-api.md", genWireAPI},
		{"sdk-go/reference.md", genSDKReference},
		{"sdk-go/messages.md", genSDKMessages},
		{"sdk-go/artifacts.md", genSDKArtifacts},
		{"sdk-go/clients.md", genSDKClients},
	}
	for _, p := range pages {
		body, err := p.content(root)
		if err != nil {
			return fmt.Errorf("%s: %w", p.rel, err)
		}
		out := filepath.Join(root, "docs", "book", "src", filepath.FromSlash(p.rel))
		if err := os.MkdirAll(filepath.Dir(out), 0o755); err != nil {
			return err
		}
		if err := os.WriteFile(out, []byte(body), 0o644); err != nil {
			return err
		}
	}
	return nil
}

func findRoot() (string, error) {
	dir, err := os.Getwd()
	if err != nil {
		return "", err
	}
	for {
		if _, err := os.Stat(filepath.Join(dir, "go.mod")); err == nil {
			return dir, nil
		}
		parent := filepath.Dir(dir)
		if parent == dir {
			return "", fmt.Errorf("docgen: go.mod not found above %s", dir)
		}
		dir = parent
	}
}

// header is the banner every generated page opens with: an HTML comment (the
// drift-check identity, invisible in the rendered book) plus a visible
// provenance note under the title.
func header(source string) string {
	return fmt.Sprintf("<!-- Code generated by cmd/docgen from %s; DO NOT EDIT. -->\n", source)
}

func provenance(source string) string {
	return fmt.Sprintf("> *Generated from `%s` — edit the source, then run `make book`.*\n", source)
}

// --- protocol/operations.md -------------------------------------------------

func genOperations(root string) (string, error) {
	raw, err := os.ReadFile(filepath.Join(root, "protocol", "methods.json"))
	if err != nil {
		return "", err
	}
	var doc struct {
		Comment    string `json:"$comment"`
		Operations []struct {
			Name     string          `json:"name"`
			Summary  string          `json:"summary"`
			Delivery string          `json:"delivery"`
			Input    json.RawMessage `json:"input"`
			Output   json.RawMessage `json:"output"`
		} `json:"operations"`
	}
	if err := json.Unmarshal(raw, &doc); err != nil {
		return "", err
	}

	var b strings.Builder
	b.WriteString(header("protocol/methods.json"))
	b.WriteString("\n# Operations\n\n")
	b.WriteString(provenance("protocol/methods.json"))
	b.WriteString("\n")
	if doc.Comment != "" {
		b.WriteString("> " + strings.TrimSpace(doc.Comment) + "\n\n")
	}
	for _, op := range doc.Operations {
		fmt.Fprintf(&b, "## `%s`\n\n", op.Name)
		fmt.Fprintf(&b, "**Delivery:** %s\n\n", op.Delivery)
		if op.Summary != "" {
			b.WriteString(op.Summary + "\n\n")
		}
		in, err := fieldTable(op.Input)
		if err != nil {
			return "", err
		}
		out, err := fieldTable(op.Output)
		if err != nil {
			return "", err
		}
		b.WriteString("**Input**\n\n" + in + "\n")
		b.WriteString("**Output**\n\n" + out + "\n")
	}
	return b.String(), nil
}

// fieldTable renders a methods.json input/output object (field -> type-hint
// string) as a markdown table, preserving the authored field order.
func fieldTable(raw json.RawMessage) (string, error) {
	pairs, err := orderedPairs(raw)
	if err != nil {
		return "", err
	}
	if len(pairs) == 0 {
		return "_None._\n", nil
	}
	var b strings.Builder
	b.WriteString("| Field | Type |\n|---|---|\n")
	for _, p := range pairs {
		var hint string
		if err := json.Unmarshal(p.Val, &hint); err != nil {
			return "", err
		}
		fmt.Fprintf(&b, "| `%s` | %s |\n", p.Key, cell(hint))
	}
	return b.String(), nil
}

